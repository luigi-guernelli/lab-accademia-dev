package livello_base.P2025_03_stampa_numeri;
/**
 * Stampa Numeri da 1 a N
 * Obiettivo: Dato un numero N, stampare tutti i numeri da 1 a N.
 *
 * Usa il ciclo for
 * Stampa i numeri su una riga
 */
public class StampaNumeri {
    public static void main (String [] args) {
        int numero = 10; // Imposta il valore di N
        for (int i=0; i<numero; i++) {
            System.out.println(i);
        }
    }
}
